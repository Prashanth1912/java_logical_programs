class Even
 {
    public static void main(String args[])
     {
      int a=2;
       switch(a%2)
        { 
           case 0:
            System.out.println("this is even");
             break;
           case 1:
           System.out.println("this is odd");
            break;
            default:
           System.out.println("wrong choice");
         }
     }
}
    
         